package streams;

public class produtoDesafio {
	
	final String nome;
	final Double valor;
	final Double desconto;
	final Boolean frete;
	
	public produtoDesafio(String nome, Double valor, Double desconto, Boolean frete) {		
		this.nome = nome;
		this.valor = valor;
		this.desconto = desconto;
		this.frete = frete;
	}
	

}
