package fundamentos;

public class OperadoresUnarios {
    public static void main(String[] args) {

        int a = 3;
        int b = a;
        int c = a + b;

        c += b; // c = c + b;
        c -= a; // c = c - a;
        c *= b; // c = c * b;
        c /= a; // c = c / a;
        c %= 2; // c = c % 2 ;

        // OPERADORES UN√ÅRIOS:

        a++; // a = a + 1;
        ++a; // a = a + 1;
    }
}
